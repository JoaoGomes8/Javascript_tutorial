// Funções como expressões

// No Javascript(JS) as funções não são estruturas mágicas, mas um tipo específico de valor.

// Vejamos a declaração de uma função:
console.clear();
function dizer_ola() {
    console.log("Olá 1!");
}

// existe outra forma de declarar uma função.

let dizer_ola2 = function(){
    console.log("Olá 2!");
}


// esta forma de definir uma função tem a designação do function expression
 