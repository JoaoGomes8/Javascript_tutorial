// como fazer uma iteração num array?

/*
Já vimos anteriormente os ciclos do JS.
Vamos olhar para o array em especial. É uma coleção de dados. 
Podemos fazer uma iteração sobre o array
*/

console.clear();

let nomes = ['joao','ana','carlos','antonio','joana','maria','francisco','marta'];

// com um ciclo for
for (let i = 0; i < nomes.length; i++) {
    console.log(nomes[i]);
}
